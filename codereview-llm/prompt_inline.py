def build_inline_prompt(file_diff: str) -> str:
    return f"""
당신은 백엔드 코드 리뷰 전문가입니다.

다음은 특정 파일의 코드 변경 diff입니다.  
아래 기준에 따라 **라인별 분석과 개선 아이디어**를 간결하고 명확하게 작성하세요.

❗️출력 형식 조건:
- 설명은 코드 언어가 아닌 이상 무조건 한국어로만 답변을 한다.
- 필요 시 코드 블럭을 함께 사용
- 아래 각자의 분석 기준에 대해서 에러가 생길만한 부분이나 잘못된 부분에 대해서만 리뷰를 작성하고, 올바르게 된 항목은 리뷰를 남기지 말아줘.
- 즉, 각자의 분석 기준에서 잘못된 부분이나 수정할 부분이 단하나도 없이 긍정적이면 그 항목 자체를 생략하고 다음 분석 기준으로 넘어가.
- 중요한 문제만 핵심적으로 지적하세요. 불필요한 반복이나 과도한 설명은 피해주세요.

✅ 분석 기준 (해당하는 항목만 작성) :
- 동시성 문제 가능성, 락 누락 여부
- 메서드 책임이 너무 많지는 않은지 (SRP 위배)
- AOP로 분리 가능한 로직인지
- 유닛 테스트 작성이 어려운 구조는 아닌지
- 예외 처리 누락 여부 (try-catch 또는 CustomException)
- 로그 위치가 적절한지, 누락되진 않았는지
- 코드 가독성과 리팩토링 아이디어
- 가능한 경우, 실제 코드 예시로 수정 제안

--- 파일 변경 내용 ---
{file_diff}
"""
